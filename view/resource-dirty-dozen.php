<h1 class="text-center">The Dirty Dozen &amp; The Clean Fifteen</h1><br>

<p>The dirty dozen consists of a list of a dozen fruits and vegetables that contain the highest amounts of pesticides. It is highly recommended 
    to shop the organic section for this produce.</p>
    
<p>The clean fifteen are the top 15 fruits and vegetables with the lowest amounts of pesticides. Of course, it is always recommended to buy
    organic. However, to save money, these foods can be purchased non-organic.</p>

<p>To get both lists for printing, check out <a href="https://www.ewg.org/foodnews/" target="_blank">ewg.org and sign up for their mailing list</a>.</p>

