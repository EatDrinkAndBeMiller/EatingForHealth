<h1 class="text-center">Reading a Nutrition Label</h1>
<p>With this diet, the goal is to eat mostly fresh and whole foods. However, you will need to buy some canned and boxed foods. 
    Here are some tips on reading a nutrition facts label&colon;</p>

<ol>
    <li><h5>Check the ingredients list first!</h5>
        <p>Ingredients are listed by quantity with the largest quantity listed first. If the first ingredient is a refined grain,
            a sugar or hydrogenated oil, that product is most likely healthy.</p>

        <p>You want ingredient lists to be as short as possible. If it's longer than 2 or 3 lines, it's highly processed. You
            want to avoid processed foods.</p>

        <p>Look for the many forms of sugar, so you can avoid them. Right now, there are 56 different names and that number is 
            constantly growing. Here is one list&colon; <a href="https://www.healthline.com/nutrition/56-different-names-for-sugar" target="_blank">
            The 56 Most Common Names for Sugar</a>.<br><br>

            Here are few names to avoid&colon;
            <div class="row">
                <div class="col">
                    <ul>
                        <li>cane sugar</li>
                        <li>malt syrup</li>
                        <li>rice syrup</li>
                        <li>barley malt</li>
                    </ul>
                </div>
                <div class="col">
                    <ul>
                        <li>maltodextrin</li>
                        <li>dextran</li>
                        <li>fructose</li>
                        <li>corn sweetener</li>
                    </ul>
                </div>
            </div>
        </p>
    </li>

    <li><h5>Check out the serving size.</h5>
        <p>The number of calories and nutrients are linked to the serving size. Sometimes, it doesn&rsquo;t seem like a product is high in
            calories until you realize the serving size is very small!</p>

        <p>When eating fruits and vegetables, the serving size is limitless. However, when consuming boxed or canned foods, pay attention
            to the serving size to avoid overdoing it.</p>
    </li>

    <li><h5>Just because it says 0, doesn&rsquo;t mean it is truly 0.</h5>
        <p>Manufacturers round on their nutritional labels, so you only see whole numbers. Just because the label says 0 grams of sugar or
        fat, does not mean it is actually 0. It could be 0.4 grams, enough to trigger your health issues. ALWAYS check the ingredients for
        the full truth.</p>
    </li>

    <li><h5>Fiber</h5>
        <p>Fiber is an essential part of your diet, which is why we are increasing the amounts of fruits and vegetables in this diet. Don&rsquo;t 
            let boxed and canned foods hinder you. Choose foods with at least 2-3 grams of fibers per serving to support your diet</p>
    </li>

    <li><h5>Cholesterol</h5>
        <p>Cholesterol in processed foods is usually linked to animal products. Check your ingredients to know the source of the cholesterol. 
            The goal is to choose box and canned food with cholesterol as close to 0 as possible.</p>
    </li>

    <li><h5>Avoid &quot;Natural Flavors&quot;</h5>
        <p>&quot;Natural flavors&quot; are popular because they can make processed foods taste much closer to the natural product. However, 
            natural flavors are manufactured in a lab and contain plant and/or animal material. Manufacturers are not required to disclose
            exactly what materials.</p>

        <p>In reality, natural flavors are much different than artificial flavors and can have negative health effects. It&rsquo;s best to avoid
            them on this diet. To read more, check out&colon; <a href="https://www.healthline.com/nutrition/natural-flavors" target="_blank">
            Natural Flavors&colon; Should You Eat Them?</a></p>
    </li>

    <li><h5>Don&rsquo;t believe the claims. Read the nutritional label</h5>
        <p>Because of marketing, many foods have healthy claims that aren&rsquo;t the complete truth. ALWAYS read the label and check all ingredients.
            Here are a few misleading claims&colon;
            <div class="row">
                <div class="col">
                    <ul>
                        <li>Natural &#40;may have only worked with a natural source at some point&#41;</li>
                        <li>Low-fat &#40;usually means extra sugar added for flavor&#41;</li>
                        <li>No added sugar &#40;check for artificial sugars&#41;</li>
                    </ul>
                </div>
                <div class="col">
                    <ul>
                        <li>Gluten-free &#40;may contain unhealthy fats and sugar&#41;</li>
                        <li>Organic &#40;may have sugar&#41;</li>
                        <li>Fortified or enriched &#40;nutrients have been added to make up those lost in processing&#41;</li>
                    </ul>
                </div>
            </div>
            <br>To find out about more misleading claims, read <a href="https://www.healthline.com/nutrition/how-to-read-food-labels" target="_blank">How to Read Food Labels Without Being Tricked</a>.</p>
    </li>
</ol>
