  <div id="stevia"> 
  <u><b>Stevia</b></u>
  <p style="text-align:left">Stevia comes from the leaves of the plant Stevia rebaudiana. It is 200 times sweeter than sugar, so only a little bit is needed. In fact, many purchase a doonk-sized teaspoon for measurement; a doonk is 1/32 teaspoon.<br><br>

  Many do not like the taste of stevia. Make sure you are purchasing a brand that does not overprocess it as this can result in a metallic taste. Stevia should be slightly green.<br><br>

  In addition, check your food label ingredients. Because stevia can be so costly to process, many companies cut it with other ingredients to bring down the cost. For example, Splenda adds sucralose which will spike your blood sugar and cause weight gain. Be sure to check for stevia as the only ingredient.<br><br>

  Stevia has a glycemic index of 0. It has virtually no calories and will not spike your blood sugar.<br><br>

  Stevia is part of the ragweed family, so if you have ragweed allergies, you may react to it like seasonal allergies.<br><br>
  </p></div>

  <div id="erythritol"> 
  <u><b>Erythritol</b></u>
  <p style="text-align:left">Erythritol is a sugar alcohol found naturally in certain fruits. Sometimes, it is a sugar alcohol derived from corn. If you have a corn sensitivity, be sure to check the source for the product you buy.<br><br>

  With only 0.24 calories per gram, erythritol has a glycemic index of 0. It will not spike your blood sugar or insulin levels.<br><br>

  As will all sugar alcohols, too much consumption at once can result in digestive issues. Keep this in mind when measuring; erythritol is only 70% as sweet as sugar. If you have a sweet tooth, you may want to add a little stevia with your erythritol.<br><br>
  </p></div>

  <div id="xylitol"> 
  <u><b>Xylitol</b></u>
  <p style="text-align:left">Xylitol is another sugar alcohol that occurs naturally in some plants. It has 40% fewer calories than sugar and a glycemic index of only 7. This is not very high compared to sugar’s GI of 60-70.<br><br>

  Xylitol’s sweetness is closer to sugar, so it makes a good 1:1 substitute. However, like all sugar alcohol, eating large amounts can result in digestive issues and gas.<br><br>

  <b>Xylitol is poisonous to dogs.</b> Keep this out of reach of your pets or consider a different alternative.<br><br>
  </p></div>

  <div id="monk"> 
  <u><b>Monk Fruit Sweetener</b></u>
  <p style="text-align:left">Extracted from the Southeast Asian fruit monk fruit, monk fruit is a natural sweetener with zero calories. It is 100-250 times sweeter than sugar. It also has a glycemic index of 0, so it won’t spike your blood sugar.<br><br>

  Monk fruit sweeteners can be purchased as granular, powdered, or liquid. However, extracts are typically 300% as sweet as sugar.<br><br>

  Monk fruit is a gourd, so if you have a gourd allergy (such as to pumpkin or cucumber), you may want to consider another alternative sweetener.<br><br>

  Be sure to check your ingredient label. Many companies add other ingredients, such as dextrose, to change the taste. Dextrose is a form of sugar. Lakanto is a brand preferred by many.<br><br>
  </p></div>

  <div id="allulose"> 
  <u><b>Allulose</b></u>
  <p style="text-align:left">Allulose is naturally found in small amounts in a few foods including raisins, figs, molasses and maple syrup. Like erythritol, it is 70% as sweet as sugar. It is also considered a sugar alcohol, so it can cause digestive issues in large amounts.<br><br>

  Calorie-wise, allulose has about 10% the calories of sugar with a glycemic index of 0. Because this is more difficult to process, it can be more costly.<br><br>
  </p></div>

  <div id="honey"> 
  <u><b>Honey</b></u>
  <p style="text-align:left">Natural honey comes from bees. When purchasing honey, be sure to check your ingredients for ONLY honey; if there is any kind of sugar or other additives, it is not really honey and you lose all health benefits.<br><br>

  Honey does have health benefits if you buy it locally since local bees use the pollen in your air. Many believe this helps with decreasing allergies. It also contains phenolic acids and flavonoids to support antioxidant activity.<br><br>

  At the same time, honey is not low in calories and has a glycemic index of 58. Only consume small doses of honey to prevent blood sugar spikes and weight gain.<br><br>
  </p></div>

  <div id="maple"> 
  <u><b>Maple Syrup</b></u>
  <p style="text-align:left">Made by cooking down the sap from maple trees, maple syrup is a thick, sugar liquid. Maple syrup contains antioxidants and minerals such as calcium, potassium, and iron.<br><br>

  However, maple syrup still contains quite a lot of sugar. This means it has a lot of calories and a glycemic index of around 54. It should be consumed in moderation.<br><br>

  When purchasing maple syrup, make sure it is “pure” or “real” with no other ingredients. Bulk buying is a great way to get it at a lower price.<br><br>
  </p></div>

  <div id="blackstrap"> 
  <u><b>Blackstrap Molasses</b></u>
  <p style="text-align:left">Molasses is made from boiling down sugar cane or sugar beet juice. When this syrup is boiled a 3rd time, it yields blackstrap molasses. It has the lowest sugar content and contains vitamins and minerals including iron, calcium, and vitamin B6.<br><br>

  Blackstrap Molasses is higher in calories than most alternatives and has a moderate glycemic index of 55. It also has a very dark flavor and usually doesn’t make a great replacement by itself.<br><br>

  Typically, it is added to granulated sweeteners in baked goods when brown sugar flavor is needed. One teaspoon mixed with 1 cup of granulated sweetener makes a good brown sugar substitute.<br><br>
  </p></div>

  <div id="coconut"> 
  <u><b>Coconut Sugar</b></u>
  <p style="text-align:left">Coconut sugar comes from the sap of coconut palm. Coconut sugar is very similar to sugar with a high-calorie count and a glycemic index slightly lower than sugar. It should be used very sparingly.<br><br>

  Coconut sugar also contains inulin, a soluble fiber that slows digestion. That’s what lowers its glycemic index. At the same time, inulin causes digestive issues for some people, so be sure to monitor that with your use of coconut sugar.<br><br>
  </p></div>
